numero1 = int(input("Insira o primeiro número inteiro: "))
numero2 = int(input("Insira o segundo número inteiro: "))

print(f"A soma dos números é: {numero1 + numero2}")
print(f"O produto dos números é: {numero1 * numero2}")
print(f"A diferença dos números é: {numero1 - numero2}")
print(f"A divisão dos números é: {numero1 / numero2}")
